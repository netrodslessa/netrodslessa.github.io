function copyDll(){
  const copyText = document.getElementById("dlls");
  copyText.select();
  copyText.setSelectionRange(0, 9999990);
  document.execCommand('copy')
  
  const tooltip = document.getElementById('myTootip');
  tooltip.innerHTML = "Copiado"
}

